# This script prints odd + even numbers using for-loop

print()

print('--- Printing Odd Number from 1-9 in Square Root ---')

for i in [1, 3, 5, 7, 9]:

    print(f'Here is the Square Root of: ', i*i)

print()

print('--- Printing Even Number from 2-10 in Square Root ---')

for j in [2, 4, 6, 8, 10]:

    print(f'Here is the Square Root of: ', j*j)

print()
