num1 = [2,4,6,8,10]

num2 = [1,3,5,7,9]

def squares(numbers):

    for num in numbers:

        print(num ** 2)

print('Square root of the following list: [2,4,6,8,10]')

squares(num1)

print('-------------------------------------------------------')

print('Square root of the following list: [1,3,5,7,9]')

squares(num2)
