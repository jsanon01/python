# This script combines 3 snippets
# 1) it prints names using title function

# print full name (first + last)
print('--------------Printing First & Last Names----------------')
first_name = 'ana'

last_name = 'pierre'

full_name = f'{first_name} {last_name}'

print(full_name.title())

# 2) same script (full_name.py) prints names location
print('--------------Printing Location Names----------------')
first = 'martissant'

last = 'carrefour'

names = f"{first} {last}"

print(names.title())

# 3) it prints apostrophe.py
print('--------------Printing Apostrophe Usage----------------')
message = "One of Python's strengths is its diversity."
print(message)
messages = 'One of Python\'s strengths is its diversity.'
print(messages)
messaj = 'Haiti est independant depuis 1804!'
print(messaj)
