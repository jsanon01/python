class Dauphin:
    def __init__(self, fname, lname):
        self.fname = fname
        self.lname = lname

    def swim(self):
        print(self.fname + ' is swimming')

    def bouncing(self):
        print(self.lname +  ' is bouncing in the water.')

kiki = Dauphin("leroi", 'polo')

kiki.swim()

kiki.bouncing()

